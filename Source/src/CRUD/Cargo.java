package CRUD;

/**
 *
 * @author joao.v.silva
 */
public class Cargo {

    private int id;
    private String descricao;

    public Cargo(int id, String descricao) {
        this.id = id;
        this.descricao = descricao;
    }

    public int getId() {
        return id;
    }

    public String getDescricao() {
        return descricao;
    }

}
